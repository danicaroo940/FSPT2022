import Mongoose from 'mongoose';

const userSchema = new Mongoose.Schema({
  username: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    minlength: 6,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  role: {
    type: String,
    default: "user",
    required: true,
  },
});

const user = Mongoose.model("user", userSchema);

export default user;